CREATE ROLE schedulers
  NOSUPERUSER INHERIT NOCREATEDB NOCREATEROLE NOREPLICATION;
CREATE ROLE scheduler LOGIN
  NOSUPERUSER INHERIT NOCREATEDB NOCREATEROLE NOREPLICATION;
GRANT schedulers TO scheduler;
DROP SCHEMA IF EXISTS public CASCADE;
